package com.sik.javawithjs.chapter4;

import java.util.Scanner;

public class FlowEx12 {
    public static void main(String[] args) {

     for(int i = 1; i <= 5; i++) {
         System.out.println(i);

         for(int j = 1; j <= 5; j++) {
             System.out.println(j);
         }

         System.out.println();
     }

    }
}
